/** SUBJECT_AVAILABILITY から「対応教科」案内文を生成する。 */
function buildSubjectsSection(): string {
  const live = SUBJECT_AVAILABILITY.filter((s) => s.status === "live").map(
    (s) => s.label
  );
  const coming = SUBJECT_AVAILABILITY.filter((s) => s.status === "coming").map(
    (s) => s.label
  );
  const liveText =
    live.length > 0
      ? `いま学習できる教科は「${live.join("・")}」です。`
      : `いまは準備中の教科が中心です。`;
  const comingText =
    coming.length > 0
      ? `「${coming.join("・")}」は順次追加していく予定で、これからどんどん教科が増えていきます。`
      : ``;
  return `- 対象は中学1〜3年生。${liveText}${comingText}
- まだ公開していない教科を聞かれたら、「今は準備中だけど、これから増えていく予定だよ」と前向きに伝える。無い教科の問題があるかのように話さない。`;
}

